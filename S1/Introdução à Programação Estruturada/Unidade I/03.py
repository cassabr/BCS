# Construa um programa que solicite três números inteiros e imprima a soma desses três números.

n1 = int(input("Digite o primeiro número inteiro: "))
n2 = int(input("Digite o segundo número inteiro: "))
n3 = int(input("Digite o terceito número inteiro: "))

soma = n1 + n2 + n3

print("A soma dos três números é: ", soma)