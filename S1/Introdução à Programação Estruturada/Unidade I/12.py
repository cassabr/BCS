# Construa um programa que receba a altura de três pessoas e calcule a média das três alturas.
# Mostre o resultado na tela.

a1 = float(input("Digite a altura 1: "))
a2 = float(input("Digite a altura 2: "))
a3 = float(input("Digite a altura 3: "))

media = (a1 + a2 + a3) / 3

print("A media das três alturas é: ", media)