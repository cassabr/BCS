# Agora, construa um programa que solicite ao usuário a temperatura em graus Celsius e a transforme em Fahrenheit. 
# Mostre o resultado na tela.

Celsius = int(input("Digite a temperatura: "))

Fahrenheit = (Celsius * 9 / 5) + 32 

print("A temperatura ",Celsius,"em Celsius é ",Fahrenheit)